import type { PhaseResult, PipelineResult, ReleaseConfig } from '../types.js';
import { writeRootReleaseDoc } from '../utils/reports.js';

export class ReleaseNotesGenerator {
  async run(config: ReleaseConfig, phases: readonly PhaseResult[]): Promise<PhaseResult> {
    const started = Date.now();
    const failed = phases.filter((p) => p.status === 'FAIL');
    const body = [
      `# Trading Platform V1 — ${config.rcLabel} Release Notes`,
      '',
      `**Release candidate:** ${config.rcLabel}`,
      `**Version tag:** \`${config.tagName}\``,
      `**Date:** ${new Date().toISOString().slice(0, 10)}`,
      `**Generated by:** \`pnpm release:rc\``,
      '',
      '## New Features',
      '',
      '### Research Platform',
      '',
      '- Historical Research Engine (US018)',
      '- Execution Simulator',
      '- Strategy Optimization',
      '- Performance Analytics / Benchmark',
      '- Research Control Center',
      '- Runtime Health',
      '',
      '### Trading Platform',
      '',
      '- **US204** Portfolio Engine',
      '- **US205** Position Engine',
      '- **US206** Order Lifecycle',
      '- **US207** Risk Engine',
      '- **US208** Paper Trading',
      '- **US209** Exchange Adapter',
      '- **US210** Live Trading Workspace',
      '- Kill Switch / emergency controls',
      '',
      '## Architecture Summary',
      '',
      '```',
      'Order → Risk → Execution → Position → Portfolio',
      '```',
      '',
      '- Paper and Live coordinators call Trading Core services.',
      '- Exchange Adapter is I/O-only.',
      '- Risk evaluates; does not execute.',
      '',
      '## Completed User Stories',
      '',
      '| ID | Title |',
      '|----|-------|',
      '| US018 | Historical Research Engine |',
      '| US204 | Portfolio Engine |',
      '| US205 | Position Engine |',
      '| US206 | Order Lifecycle |',
      '| US207 | Risk Engine |',
      '| US208 | Paper Trading |',
      '| US209 | Exchange Adapter |',
      '| US210 | Live Trading Workspace (+ Kill Switch) |',
      '',
      '## Known Limitations',
      '',
      ...(failed.length > 0
        ? failed.flatMap((p) => p.criticalIssues.map((issue) => `- [${p.name}] ${issue}`))
        : ['- None recorded by the release pipeline for this candidate.']),
      '',
      '## Deployment Notes',
      '',
      '1. `pnpm install`',
      '2. Configure `.env` from `.env.example` with a strong `JWT_SECRET`',
      '3. `pnpm --filter @trp/api exec prisma generate`',
      '4. `pnpm --filter @trp/api exec prisma migrate deploy`',
      '5. `pnpm build && pnpm --filter @trp/api start`',
      '',
      '## Migration Notes',
      '',
      '- Apply Prisma migrations under `apps/api/prisma/migrations/` including US018 and US204–US210 / kill switch.',
      '- Verify with `prisma migrate status` after deploy.',
      '',
      '## Pipeline Phase Snapshot',
      '',
      '| Phase | Status |',
      '|-------|--------|',
      ...phases.map((p) => `| ${p.name} | ${p.status} |`),
      '',
    ].join('\n');

    const reportPath = await writeRootReleaseDoc(
      config,
      `${config.rcLabel}-RELEASE-NOTES.md`,
      body,
    );

    return {
      id: 'release-notes',
      name: 'Release Notes',
      status: 'PASS',
      durationMs: Date.now() - started,
      summary: `Release notes generated at ${reportPath}`,
      criticalIssues: [],
      warnings: [],
      recommendations: [],
      reportPath,
    };
  }
}
